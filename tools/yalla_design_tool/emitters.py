def generate_cmp_images(tokens: dict, out_path: Path) -> None:
    # ThemedImage.kt
    lines = [
        "package uz.yalla.design.image",
        "",
        "import org.jetbrains.compose.resources.DrawableResource",
        "import uz.yalla.resources.Res",
    ]
    images = tokens["themedImages"]["images"]
    for image in images:
        lines.append(f"import uz.yalla.resources.{image['light']}")
        lines.append(f"import uz.yalla.resources.{image['dark']}")
    lines.append("")
    lines.append("// Generated by yalla-design. Do not edit manually.")
    lines.append("")
    lines.append("enum class ThemedImage(")
    lines.append("    val light: DrawableResource,")
    lines.append("    val dark: DrawableResource")
    lines.append(") {")
    for image in images:
        lines.append(f"    {image['name']}(Res.drawable.{image['light']}, Res.drawable.{image['dark']}),")
    lines.append("}")
    write_text(out_path / "ThemedImage.kt", "\n".join(lines) + "\n")

    # ThemedPainter.kt
    painter_lines = [
        "package uz.yalla.design.image",
        "",
        "import androidx.compose.runtime.Composable",
        "import androidx.compose.ui.graphics.painter.Painter",
        "import org.jetbrains.compose.resources.painterResource",
        "import uz.yalla.design.theme.LocalIsDark",
        "",
        "@Composable",
        "fun themedPainter(image: ThemedImage): Painter {",
        "    val resource = if (LocalIsDark.current) image.dark else image.light",
        "    return painterResource(resource)",
        "}",
    ]
    write_text(out_path / "ThemedPainter.kt", "\n".join(painter_lines) + "\n")

