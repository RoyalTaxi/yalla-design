        "import androidx.compose.ui.text.TextStyle",
        "",
        "@Immutable",
        "data class FontScheme(",
        "    val title: Title,",
        "    val body: Body,",
        "    val custom: Custom",
        ") {",
        "    @Immutable",
        "    data class Title(",
        "        val xLarge: TextStyle,",
        "        val large: TextStyle,",
        "        val base: TextStyle",
        "    )",
